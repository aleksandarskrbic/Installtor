package configurator.model.parameter;

public class LogicalParameter extends AbstractParameter {

	private static final long serialVersionUID = -3673798841540566465L;
	
	private Boolean value;
	
	public LogicalParameter(String name) {
		super(ParameterType.LOGICAL, name);
	}

	@Override
	public void setValue(Object value) {
		this.value = (Boolean) value;
	}

	@Override
	public Boolean getValue() {
		return value;
	}
}
